package Operators;

public class Arithmeticoperator4 {
	
	public static void main(String[] arg) {
		
		int p=50,q=40;
		
	System.out.println("p+q="+(p+q));
		
		System.out.println("p-q="+(p-q));
		
		System.out.println("p*q="+(p*q));
		
		System.out.println("p/q="+(p+q));
		
		System.out.println("p%q="+(p%q));
	

	}

}
