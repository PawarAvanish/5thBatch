package Operators;

public class DecreIncre5 {
	
	public static void main(String[] arg) {
		
		int p=46,q=56;
		
		
		System.out.println(p>q); //greater than
		System.out.println(p<=q);//mismatch
		
	System.out.println(p<q); //less than
	
	
	System.out.println(p<=q); //less than equals
	
	
	System.out.println(p>=q); //greater than
	
	
	System.out.println(p==q); //==operator
	
	
	System.out.println(p!=q); //!=not equals

	}

}
