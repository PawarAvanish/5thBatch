package Operators;

public class Arithmeticoperator3 {
	
	public static void main(String[] arg) {
		
		int a=10, b=20;
		
		
		System.out.println("a+b="+(a+b));
		
		System.out.println("a-b="+(a-b));
		
		System.out.println("a*b="+(a*b));
		
		System.out.println("a/b="+(a+b));
		
		System.out.println("a%b="+(a%b));
	

	}

}
