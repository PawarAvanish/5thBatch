package Logicaloperator;

public class logicaloperator5 {
	
	public static void main(String[] arg) {
		
		System.out.println((20>3) &&(12>7));//true
		
		System.out.println((20>3) &&(12<7));//false
		
		System.out.println((10>3) ||(12>7));//true
		
		System.out.println((20<3) ||(12<7));//false
		
		System.out.println(!(20==3));//true
		
		System.out.println(!(4>1));//false



	}

}
